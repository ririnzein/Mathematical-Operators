#include <stdio.h>

int main ()
{
    int x = 20;
    int y = 10;

    int penjumlahan = x + y;
    printf("%d\n", penjumlahan);

    int pengurangan = x - y;
    printf("%d\n", pengurangan);

    int perkalian = x * y;
    printf("%d\n", perkalian);

    int pembagian = x / y ;
    printf ("%d\n", pembagian);
    return 0;

}
