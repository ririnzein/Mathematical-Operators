#include <stdio.h>

int main ()
{
    int y = 50;
    int x = 20;

    int penjumlahan = y + x;
    printf("%d\n", penjumlahan);

    int perkalian = y * x;
    printf("%d\n", perkalian);

    int pembagian = y / x;
    printf("%d\n", pembagian);

    int pengurangan = y - x;
    printf("%d\n", pengurangan);

    return 0;


}
